def decode_neighbors(encoded: str) -> list:
    """Decode base64 encoded neighbors."""
    if not encoded:
        return []
    binary = base64.b64decode(encoded)
    count = len(binary) // 4
    return list(struct.unpack(f"<{count}I", binary))

def load_tiles() -> Tuple[List[dict], dict]:
    """Load all tiles from unified files."""
    print("Loading unified tile files...")
    
    # Load metadata first
    try:
        with open(METADATA_FILE, "r") as f:
            metadata = json.load(f)
        print(f"  Metadata loaded:")
        print(f"    Total tiles: {metadata.get('total_tiles', 0):,}")
        print(f"    Base points: {metadata.get('base_points', 0):,}")
        print(f"    Neighbor count: {metadata.get('neighbor_count', 0)}")
    except FileNotFoundError:
        print(f"  Warning: {METADATA_FILE} not found")
        metadata = {}
    except json.JSONDecodeError:
        print(f"  Warning: {METADATA_FILE} is corrupted")
        metadata = {}
    
    # Load static data
    try:
        with open(STATIC_FILE, "r") as f:
            static_data = json.load(f)
        print(f"  Loaded {len(static_data):,} tiles from {STATIC_FILE}")
    except FileNotFoundError:
        print(f"  ERROR: {STATIC_FILE} not found")
        return [], {}
    except json.JSONDecodeError:
        print(f"  ERROR: {STATIC_FILE} is corrupted")
        return [], {}
    
    # Load dynamic data
    try:
        with open(DYNAMIC_FILE, "r") as f:
            dynamic_data = json.load(f)
        print(f"  Loaded {len(dynamic_data):,} entries from {DYNAMIC_FILE}")
    except FileNotFoundError:
        print(f"  Warning: {DYNAMIC_FILE} not found, using defaults")
        dynamic_data = []
    except json.JSONDecodeError:
        print(f"  Warning: {DYNAMIC_FILE} is corrupted, using defaults")
        dynamic_data = []
    
    # Build dynamic lookup
    dyn_lookup = {item["id"]: item for item in dynamic_data}
    
    # Combine static and dynamic data
    tiles = []
    
    bar = ProgressBar(len(static_data), "  Loading tiles")
    
    for s in static_data:
        tid = s["id"]
        d = dyn_lookup.get(tid, {})
        
        tiles.append({
            "id": tid,
            "neighbors": decode_neighbors(s.get("neighbors_b64", "")),
            "temperature": s.get("temperature", ""),
            "precipitation": s.get("precipitation", ""),
            "terrain": s.get("terrain", ""),
            "vegetation": d.get("vegetation", ""),
            "population": d.get("population", 0),
            "buildings": d.get("buildings", [])
        })
        bar.update(1)
    
    bar.close()
    
    # Sort by ID for consistent ordering
    tiles.sort(key=lambda t: t["id"])
    
    print(f"\n  Loaded {len(tiles):,} tiles")
    
    # Display terrain type counts
    terrain_counts = {}
    for tile in tiles:
        terrain = tile.get("terrain", "")
        terrain_counts[terrain] = terrain_counts.get(terrain, 0) + 1
    
    if terrain_counts:
        print(f"\n  Terrain distribution:")
        for terrain, count in sorted(terrain_counts.items(), key=lambda x: -x[1])[:10]:
            print(f"    '{terrain}': {count:,} tiles")
    
    return tiles, metadata


