def image_xy_to_sphere_batch(width: int, height: int) -> np.ndarray:
    """Generate sphere coordinates for all pixels."""
    print(f"\nGenerating pixel coordinates...")
    bar = ProgressBar(height, "  Building coordinate grid")
    
    x_img = np.arange(width)
    y_img = np.arange(height)
    xx, yy = np.meshgrid(x_img, y_img)
    bar.update(height)
    bar.close()
    
    print("  Converting to sphere coordinates...")
    lon = (xx / width) * 360.0 - 180.0
    lat = 90.0 - (yy / height) * 180.0
    
    lat_rad = np.radians(lat)
    lon_rad = np.radians(lon)
    
    y = np.sin(lat_rad)
    r = np.cos(lat_rad)
    x = np.cos(lon_rad) * r
    z = np.sin(lon_rad) * r
    
    points = np.column_stack((x.ravel(), y.ravel(), z.ravel()))
    print(f"  Generated {len(points):,} points")
    return points

def render_world_fast(tiles: List[dict], metadata: dict):
    """Main rendering function."""
    print("\n" + "="*60)
    print(f"UNIFIED WORLD RENDERER - Mode: {RENDER_MODE}")
    print(f"Total tiles loaded: {len(tiles):,}")
    print("="*60)
    
    if not tiles:
        print("No tiles to render! Check RENDER_* flags.")
        return
    
    # Get base points from metadata or compute from max ID
    base_points = metadata.get("base_points", 0)
    if base_points == 0 and tiles:
        base_points = max(t["id"] for t in tiles) + 1
        print(f"\n  Base points inferred from max ID: {base_points:,}")
    
    print(f"\nBase resolution: {base_points:,} Fibonacci points")
    
    # Display tile counts by type
    type_counts = {}
    for tile in tiles:
        tile_type = tile.get("type", "unknown")
        type_counts[tile_type] = type_counts.get(tile_type, 0) + 1
    
    print(f"\nTile counts by type:")
    for tile_type, count in sorted(type_counts.items()):
        print(f"  {tile_type}: {count:,} tiles")
    
    # ID statistics
    all_ids = [t["id"] for t in tiles]
    print(f"\nID statistics:")
    print(f"  Min ID: {min(all_ids):,}")
    print(f"  Max ID: {max(all_ids):,}")
    print(f"  Actual tiles: {len(tiles):,}")
    
    # Regenerate positions
    print("\n" + "="*60)
    print("REGENERATING POSITIONS")
    print("="*60)
    regenerate_positions(tiles, base_points)
    
    # Build KD-tree
    print("\n" + "="*60)
    print("BUILDING KD-TREE")
    print("="*60)
    positions = np.array([[t["x"], t["y"], t["z"]] for t in tiles], dtype=np.float64)
    tree = cKDTree(positions)
    print(f"  KD-tree built with {len(positions):,} points")
    
    # Generate query points
    query_points = image_xy_to_sphere_batch(IMAGE_WIDTH, IMAGE_HEIGHT)
    
    # Batch query
    print("\n" + "="*60)
    print("QUERYING NEAREST TILES")
    print("="*60)
    query_bar = ProgressBar(len(query_points), "  KD-tree query", width=40)
    
    chunk_size = 1000000
    all_indices = []
    
    for i in range(0, len(query_points), chunk_size):
        chunk = query_points[i:i+chunk_size]
        distances, indices = tree.query(chunk, k=1)
        all_indices.extend(indices)
        query_bar.update(len(chunk))
    
    query_bar.close()
    indices = np.array(all_indices, dtype=np.int32)
    
    # Build color array based on render mode
    print("\n" + "="*60)
    print(f"BUILDING COLOR ARRAY (Mode: {RENDER_MODE})")
    print("="*60)
    
    if RENDER_MODE == "type":
        # Type-based rendering
        print(f"  Creating color lookup table for {len(tiles):,} tiles...")
        tile_colors = np.zeros((len(tiles), 3), dtype=np.uint8)
        
        color_bar = ProgressBar(len(tiles), "  Building color lookup")
        for i, tile in enumerate(tiles):
            tile_type = tile.get("type", "unknown")
            rgb = TYPE_COLORS.get(tile_type, TYPE_COLORS["unknown"])
            tile_colors[i] = rgb
            color_bar.update(1)
        color_bar.close()
        
        # Fast vectorized lookup
        print(f"  Applying colors to {len(indices):,} pixels...")
        img_array = tile_colors[indices]
        
    elif RENDER_MODE == "random":
        # Random colors based on tile ID
        max_id = max(t["id"] for t in tiles)
        print(f"  Creating random colors for IDs 0..{max_id:,}")
        
        random_bar = ProgressBar(max_id + 1, "  Generating random colors", width=40)
        
        rng = np.random.RandomState(42)
        random_colors = np.zeros((max_id + 1, 3), dtype=np.uint8)
        
        chunk = 100000
        for start in range(0, max_id + 1, chunk):
            end = min(start + chunk, max_id + 1)
            random_colors[start:end] = rng.randint(50, 256, size=(end - start, 3), dtype=np.uint8)
            random_bar.update(end - start)
        
        random_bar.close()
        
        # Build tile color lookup
        print(f"  Building tile color lookup...")
        tile_colors = np.zeros((len(tiles), 3), dtype=np.uint8)
        color_bar = ProgressBar(len(tiles), "  Mapping tile IDs to colors")
        
        for i, tile in enumerate(tiles):
            tile_colors[i] = random_colors[tile["id"]]
            color_bar.update(1)
        color_bar.close()
        
        # Apply to pixels
        print(f"  Applying colors to {len(indices):,} pixels...")
        img_array = tile_colors[indices]
    
    elif RENDER_MODE == "population":
        # Population-based gradient
        print(f"  Finding population range...")
        populations = [t.get("population", 0) for t in tiles]
        max_pop = max(populations) if populations else 1
        
        print(f"  Population range: 0 to {max_pop:,}")
        print(f"  Creating color lookup table...")
        
        tile_colors = np.zeros((len(tiles), 3), dtype=np.uint8)
        color_bar = ProgressBar(len(tiles), "  Building color lookup")
        
        for i, tile in enumerate(tiles):
            pop = tile.get("population", 0)
            rgb = get_population_color(pop, max_pop)
            tile_colors[i] = rgb
            color_bar.update(1)
        color_bar.close()
        
        # Apply to pixels
        print(f"  Applying colors to {len(indices):,} pixels...")
        img_array = tile_colors[indices]
    
    else:
        raise ValueError(f"Invalid render mode: {RENDER_MODE}")
    
    # Reshape to 2D image
    print("\n" + "="*60)
    print("CREATING FINAL IMAGE")
    print("="*60)
    print("  Reshaping to 2D...")
    img_array = img_array.reshape(IMAGE_HEIGHT, IMAGE_WIDTH, 3)
    
    print("  Creating PIL image...")
    image = Image.fromarray(img_array, "RGB")
    
    print(f"  Saving {OUTPUT_FILE}...")
    image.save(OUTPUT_FILE)
    
    print("\n" + "="*60)
    print("✓ RENDER COMPLETE!")
    print(f"  Output: {OUTPUT_FILE}")
    print(f"  Size: {IMAGE_WIDTH}x{IMAGE_HEIGHT}")
    print("="*60)


