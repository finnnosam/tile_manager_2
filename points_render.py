def get_population_color(population: int, max_pop: int) -> Tuple[int, int, int]:
    """Map population value to a color gradient."""
    if max_pop == 0:
        return POPULATION_COLORS[0]
    
    # Normalize population to 0-1 range
    norm = min(1.0, population / max_pop)
    
    # Find which color segment
    segment_count = len(POPULATION_COLORS) - 1
    segment = norm * segment_count
    idx = int(segment)
    frac = segment - idx
    
    if idx >= segment_count:
        return POPULATION_COLORS[-1]
    
    # Interpolate between colors
    c1 = POPULATION_COLORS[idx]
    c2 = POPULATION_COLORS[idx + 1]
    
    return (
        int(c1[0] * (1 - frac) + c2[0] * frac),
        int(c1[1] * (1 - frac) + c2[1] * frac),
        int(c1[2] * (1 - frac) + c2[2] * frac)
    )


