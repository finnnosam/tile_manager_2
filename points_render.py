def main():
    print("="*60)
    print("WORLD RENDERER")
    print("Renders terrain-based tiles")
    print("="*60)
    
    start_time = time.time()
    
    tiles, metadata = load_tiles()
    if not tiles:
        print("\nNo tiles loaded!")
        print("Make sure:")
        print("  1. tiles_static.json exists")
        print("  2. tiles_metadata.json exists")
        return
    
    render_world_fast(tiles, metadata)
    
    elapsed = time.time() - start_time
    print(f"\nTotal time: {elapsed:.1f} seconds")


if __name__ == "__main__":
    main()